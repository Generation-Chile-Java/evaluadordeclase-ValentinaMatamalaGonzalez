package lunes2608.buclewhile;

public class BucleWhile {
    //El bucle while se ejecuta indefinidamente siempre y cuando la condicion se evalue com verdadera
    public static void main(String[] args) {
        //contador de despegue
        //while signifca que "mientras" la condicion sea true, va a seguir iterando

                Integer variableAculable = 10;

                while (variableAculable >= 0) {

                    System.out.println(variableAculable);
                    variableAculable--;
                }
    }
}
